    std::int64_t get_timestamp_ns() {
        // Get current time with nanosecond precision. Use system_clock instead of steady_clock to measure duration across different servers.
        const auto now = std::chrono::system_clock::now();

        // Convert to nanoseconds since epoch.
        return std::chrono::duration_cast<std::chrono::nanoseconds>(now.time_since_epoch()).count();
    }

    std::string get_env(const char* name) {
        const char* value = std::getenv(name);
        if (!value) {
            throw std::runtime_error(std::string("Environment variable ") + name + " not found");
        }
        return value;
    }

